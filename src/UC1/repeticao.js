// Estrutura de repetição FOR

for(let i=0;i<=100;i=i+3) {
    console.log(`Loop ${i}`)
}

for(let i=100; i>=0; i--) {
    console.log(`Loop ${i}`);
}

// Estrutura de repetição WHILE
let = i=11;
while(i<=10) {
    console.log(`While ${i}`);
    i++;
}

// Estrutura de repetição DO-WHILE
let j=0;
do{
    console.log(`DO-WHILE ${j}`)
    j++;
}while(j<=10);